const express = require("express");
const router = express.Router();
const { protect } = require("../middleware/auth");
// TODO: const ctrl = require("../controllers/boards");
router.get("/",       protect, (req, res) => res.json({ message: "boards GET /" }));
router.post("/",      protect, (req, res) => res.json({ message: "boards POST /" }));
router.get("/:id",    protect, (req, res) => res.json({ message: "boards GET /:id" }));
router.put("/:id",    protect, (req, res) => res.json({ message: "boards PUT /:id" }));
router.delete("/:id", protect, (req, res) => res.json({ message: "boards DELETE /:id" }));
module.exports = router;
