const express = require("express");
const router = express.Router();
const { protect } = require("../middleware/auth");
// TODO: const ctrl = require("../controllers/users");
router.get("/",       protect, (req, res) => res.json({ message: "users GET /" }));
router.post("/",      protect, (req, res) => res.json({ message: "users POST /" }));
router.get("/:id",    protect, (req, res) => res.json({ message: "users GET /:id" }));
router.put("/:id",    protect, (req, res) => res.json({ message: "users PUT /:id" }));
router.delete("/:id", protect, (req, res) => res.json({ message: "users DELETE /:id" }));
module.exports = router;
